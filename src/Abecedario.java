
public class Abecedario {

	public static void main(String[] args) {
		
		
	
		for (int i = 97; i< 97+27-1; i++) {
	
			System.out.print((char) i + " ");
	
}
		
		// while 
			
		int contador = 97;
		
		while (contador < 97+27-1) {
			
			
			System.out.print((char) contador + " ");
			contador = contador +1;
		}
	}

}
